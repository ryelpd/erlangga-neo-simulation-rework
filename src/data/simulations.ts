export function getSimulationById(id: string): Simulation | undefined {
  return simulations.find((sim) => sim.id === id);
}

export function getSimulationsByCategory(category: Category): Simulation[] {
  return simulations.filter((sim) => sim.category === category);
}

export function searchSimulations(query: string): Simulation[] {
  const lowerQuery = query.toLowerCase();
  return simulations.filter(
    (sim) =>
      sim.title.toLowerCase().includes(lowerQuery) ||
      sim.id.toLowerCase().includes(lowerQuery) ||
      sim.tags.some((tag) => tag.toLowerCase().includes(lowerQuery))
  );
}
